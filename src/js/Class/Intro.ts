//--------------------- Parameter properties ---------------------
/**
 * 'Parameter properties' let you create and initialize a member in one place
 */
class Animal {
    constructor(private name: string) { }
    move(distanceInMeters: number) {
        console.log(`${this.name} moved ${distanceInMeters}m.`);
    }
}
